public class Details {

    private String name;
    private String roll;
    private String email;
    private String nick;
    private String branch;
    private String branchm;
    private String debut;
    private String dob;
    private String father;
    private String gender;
    private String logo;
    private String mother;
    private String section;
    private String sectionm;

    public Details(){

    }

    public Details(String name, String roll, String email, String nick, String branch, String branchm, String debut, String dob, String father, String gender, String logo, String mother, String section, String sectionm) {
        this.name = name;
        this.roll = roll;
        this.email = email;
        this.nick = nick;
        this.branch = branch;
        this.branchm = branchm;
        this.debut = debut;
        this.dob = dob;
        this.father = father;
        this.gender = gender;
        this.logo = logo;
        this.mother = mother;
        this.section = section;
        this.sectionm = sectionm;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRoll() {
        return roll;
    }

    public void setRoll(String roll) {
        this.roll = roll;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNick() {
        return nick;
    }

    public void setNick(String nick) {
        this.nick = nick;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public String getBranchm() {
        return branchm;
    }

    public void setBranchm(String branchm) {
        this.branchm = branchm;
    }

    public String getDebut() {
        return debut;
    }

    public void setDebut(String debut) {
        this.debut = debut;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getFather() {
        return father;
    }

    public void setFather(String father) {
        this.father = father;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getMother() {
        return mother;
    }

    public void setMother(String mother) {
        this.mother = mother;
    }

    public String getSection() {
        return section;
    }

    public void setSection(String section) {
        this.section = section;
    }

    public String getSectionm() {
        return sectionm;
    }

    public void setSectionm(String sectionm) {
        this.sectionm = sectionm;
    }
}
